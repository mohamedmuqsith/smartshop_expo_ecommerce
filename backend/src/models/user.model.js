import mongoose from "mongoose";

const addressSchema = new mongoose.Schema({
    label:{
        type:String,
        required:true
    },
    fullName:{
        type:String,
        required:true
    },
    StreetAddress:{
        type:String,
        required:true
    },
    city:{
        type:String,
        required:true
    },
    province:{
        type:String,
        required:true
    },
    zipCode:{
        type:String,
        required:true
    },
    phoneNumber:{
        type:String,
        required:true
    },
    isDefault:{
        type:Boolean,
        default:false
    }
})

const userSchema = new mongoose.Schema({
    email:{
        type:String,
        required:true,
        unique:true
    },
     name:{
        type:String,
        required:true
     },
     imageURL:{
        type:String,
        default:""
     },
     clerkId:{
        type:String,
        unique:true,
        required:true
     },
     addresses:[addressSchema],
     wishlist:[
        {
            type:mongoose.Schema.Types.ObjectId,
            ref:"Product"
        }
     ],
}, {timestamps:true}
)

export const User = mongoose.model("User", userSchema)